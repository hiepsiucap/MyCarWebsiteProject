export function tinhSoNgay(start, end) {
   if(!start || !end) return 0;
    var timeDiff = Math.abs(end.getTime() - start.getTime());
    var diffDays = Math.ceil(timeDiff / (1000 * 3600 * 24))+1; 
    return diffDays;
}
